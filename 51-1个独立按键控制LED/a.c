# include <8052.h>
#define KEY P3_2
#define LED P1_2

/*------------------------------------------------
                    主函数
------------------------------------------------*/
void main (void)
{
                  
KEY=1; //按键输入端口电平置高
while (1)         //主循环
  {

  if(!KEY)  //如果检测到低电平，说明按键按下
    LED=0;
  else
    LED=1;  //这里使用if判断，如果按键按下led点亮，否则熄灭
	        //上述4句可以用一句替代 LED=KEY;
                     //主循环中添加其他需要一直工作的程序
  }
}
